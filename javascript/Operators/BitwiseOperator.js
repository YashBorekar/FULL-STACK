// Bitwise AND
let x = 5;  // 0101
let y = 3;  // 0011
console.log(x & y); // 1 (0001)

// Bitwise OR
console.log(x | y); // 7 (0111)

// Bitwise NOT
console.log(~x);    // -6 (inverts all bits)

// Bitwise XOR
console.log(x ^ y); // 6 (0110) 

// right shift
console.log(x >> 1); // 2 (0010)

// left shift
console.log(x << 1); // 10 (1010)
