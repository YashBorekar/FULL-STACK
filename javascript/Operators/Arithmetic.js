// Arithmetic Operators

// Addition
let result = 5 + 3; // result is 8
console.log("Addition Result:", result);

// Substraction
let subtractedValue = 10 - 5; // subtractedValue is 5
console.log("Subtraction Result:", subtractedValue);

// Multiplication
let product = 4 * 6; // product is 24
console.log("Multiplication Result:", product);

// Division
let dividedValue = 24 / 3; // dividedValue is 8
console.log("Division Result:", dividedValue);

// Modulus
let modulusValue = 10 % 3; // modulusValue is 1
console.log("Modulus Result:", modulusValue);

// Exponentiation
let expoValue = 2 ** 3; // result is 8 (2 to the power of 3)
console.log("Exponentiation Result:", expoValue);

// Increment
let incrementValue = 5;
incrementValue++;
console.log("Increment Result:", incrementValue);

// Decrement
let decrementValue = 5;
decrementValue--; 
console.log("Decrement Result:", decrementValue);

// Operator Precedence
/*
   1. Parentheses/Brackets ()
   2. Exponents/Orders `**`
   3. Multiplication * and Division /  (Left To Right)
   4. Addition + and Subtraction - (Left To Right)
*/ 

let cal = (8*(5+5)/5*(10%2)-3);
console.log("Calculation Result:", cal);
